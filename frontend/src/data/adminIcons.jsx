// Centralized admin icon library. Every admin component
// imports from here instead of defining its own Icon object.
//
// Icons are grouped into:
//   core      – used by the admin shell (sidebar, header, layout)
//   nav       – workspace nav + selector (extends core)
//   dashboard – dashboard-specific (charts, widgets, search)
//   actions   – common action icons (CRUD, bulk, export, etc.)
//   misc      – one-off icons used by individual pages
//
// All icons follow the same baseline:
//   viewBox="0 0 24 24" fill="none" stroke="currentColor"
//   strokeWidth="2" strokeLinecap="round" strokeLinejoin="round"
// ──────────────────────────────────────────────────────────

